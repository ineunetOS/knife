package com.ineunet.knife.core.security;

/**
 * For security. e.g. for security Realm, security server.<br>
 * An AccountModel may be a user AccountModel or a tenant AccountModel.
 * @author Hilbert Wang
 * @since 2.0.0
 */
public class AccountModel {

	private Long id; // accountId
	private String account;
	private String passwd;
	private Long orgId;
	private Long tenantId;
	
	public AccountModel() {}
	
	public AccountModel(Long id, String account, String passwd, Long tenantId) {
		this.id = id;
		this.account = account;
		this.passwd = passwd;
		this.tenantId = tenantId;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getPasswd() {
		return passwd;
	}

	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}

	public Long getTenantId() {
		return tenantId;
	}

	public void setTenantId(Long tenantId) {
		this.tenantId = tenantId;
	}

	public Long getOrgId() {
		return orgId;
	}

	public void setOrgId(Long orgId) {
		this.orgId = orgId;
	}
	
}
