package com.ineunet.knife.core.param;

/**
 * 
 * @author Hilbert Wang
 * 
 * @since 1.2.0
 *
 */
public class IdCodeName extends IdName {

	private String code;
	
	public IdCodeName() {
		super();
	}
	
	public IdCodeName(Object id, String code, String name) {
		super(id, name);
		this.code = code;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((code == null) ? 0 : code.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		IdCodeName other = (IdCodeName) obj;
		if (code == null) {
			if (other.code != null)
				return false;
		} else if (!code.equals(other.code))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "IdCodeName [code=" + code + "]";
	}
	
}
