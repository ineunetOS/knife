package com.ineunet.knife.core.easyui;

import java.util.List;

/**
 * 
 * @author Hilbert
 * @param <T>
 */
public class EasyUIGridData<T> {

	private int total;
	private List<T> rows;
	
	public EasyUIGridData() {}
	
	public EasyUIGridData(int total, List<T> rows) {
		this.total = total;
		this.rows = rows;
	}

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public List<T> getRows() {
		return rows;
	}

	public void setRows(List<T> rows) {
		this.rows = rows;
	}

}
